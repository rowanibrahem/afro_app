class FavModel {
  var id;
  String? title;
  String? noOfHours;
  String? noOfLessons;
  String? image;

  FavModel(
      {required this.title,
      required this.image,
      required this.noOfHours,
      required this.noOfLessons,
      required this.id}) {
    image = image;
    title = title;
    id = id;
    noOfHours = noOfHours;
    noOfLessons = noOfLessons;
  }
}
